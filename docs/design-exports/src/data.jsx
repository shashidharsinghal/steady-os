const daily = Array.from({ length: 30 }, (_, i) => {
  const date = new Date(today);
  date.setDate(date.getDate() - (29 - i));
  const dow = date.getDay();
  const baseline = dow === 0 || dow === 6 ? 142000 : 96000; // weekend lift
  const noise = Math.sin(i * 0.7) * 12000 + Math.cos(i * 1.3) * 6000;
  const sales = Math.round((baseline + noise + i * 600) * 100); // paise
  const orders = Math.round(sales / 100 / 380 + (Math.random() - 0.5) * 20);
  const aov = Math.round(sales / orders);
  const repeat = Math.round(orders * (0.32 + (Math.random() - 0.5) * 0.08));
  // COGS ~28% of sales, daily fixed-expense allocation ~₹47k (1.4 Cr/30 outlets)
  const cogs = Math.round(sales * (0.27 + (Math.random() - 0.5) * 0.04));
  const fixedDaily = 4700000; // paise per day allocated
  const profit = sales - cogs - fixedDaily;
  const profitPct = (profit / sales) * 100;
  return {
    date: date.toISOString().slice(0, 10),
    label: date.toLocaleDateString("en-IN", { day: "2-digit", month: "short" }),
    dow,
    dowLabel: ["Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"][dow],
    sales,
    orders,
    aov,
    repeat,
    cogs,
    profit,
    profitPct,
  };
});

